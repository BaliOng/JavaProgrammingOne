package Chapter1;

/**
 * Program that displays the area and perimeter of a circle with a radius of 5.5
 *
 * @author Bali Ong
 */
public class C1_8 {

    public static void main(String[] args) {
        System.out.println("The area of a circle with a radius of 5.5:");
        System.out.println(5.5 * 5.5 * 3.14);
        System.out.println("The perimeter of a circle with a radius of 5.5:");
        System.out.println(2 * 5.5 * 3.15);
    }

}
